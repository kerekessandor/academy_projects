#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	char *ptr;

	ptr = (char*)s;
	while (*ptr != '\0')
	{
		ft_putchar_fd(*ptr, fd);
		ptr++;
	}
}
